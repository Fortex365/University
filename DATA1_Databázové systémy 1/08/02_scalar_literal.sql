Skalární výraz je výraz, jehož hodnota náleží do skalárního typu (domény).

Typ hodnoty skalárního výrazu je vždy stejný a nazývá se typem skalárního výrazu.

Hodnota skalárního výrazu se určuje vzhledem k n-tici. 
*/

/*
Zápis hodnoty určitého typu se nazývá literál.

Zápis celého čísla je literál typu integer.

Například: 1, 10, -2.

Zápis řetězce je literál typu varchar(x).

Například: '', 'Anna', 'Kosinova'.
*/

/*
Literál je skalární výraz, který má v každé n-tici stále stejnou hodnotu.

Například literál 1 se v každé n-tici vyhodnotí na číslo jedna.
*/